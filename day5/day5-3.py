"""
百鸡百钱
我国古代数学家张丘建在《算经》一书中提出的数学问题：鸡翁一值钱五，鸡母一值钱三，鸡雏三值钱一。百钱买百鸡，问鸡翁、鸡母、鸡雏各几何？

Author:lkb
"""

for x in range(0, 100//5 +1):
    for y in range(0, 100//3 +1):
        for z in range(0, 300//1 + 1):
            if (x+y+z) == 100 and (15*x+9*y+z) == 300 :
                print('公鸡有%d，母鸡有%d, 小鸡有%d' % (x,y,z))